public class ArrayBilangan25{

    public static void main(String[] args) {
        //deklarasi dan inisialisasi elemen array
        int[] bil = new int [4];
        
        //mengisi masing2 elemen array
        bil [0] = 5;
        bil [1] = 13;
        bil [2] = -7;
        bil [3] = 17;

        //mengubah statement program 
        for (int i = 0; i <4; i++) {
            System.out.println(bil[i]);
        }
    }
}

    